package com.learning.doublecolon.operator;

public class Test {

	public static void show() {
		for (int i = 1; i <= 5; i++) {
			System.out.println(i + ": Hello from show()");
		}
	}

	public void display() {
		for (int i = 1; i <= 5; i++) {
			System.out.println(i + ": Welcome to display()");
		}
	}
}
